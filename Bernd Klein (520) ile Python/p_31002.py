import numpy as np

x = np.array ([1, 2, 3])
y = np.array ([-7,8,9])
z = np.dot (x, y)
kosAçý = z / (np.sqrt ((x * x).sum()) * np.sqrt ((y * y).sum()) )
açýRadyan = np.arccos (kosAçý)
açýDerece = açýRadyan * 180 / np.pi

print ("x dizisi:", x)
print ("y dizisi:", y)
print ("x.y deðeri:", z)
print ("cos (x,y) radyan açýsý:", açýRadyan)
print ("cos (x,y) derece açýsý:", açýDerece)
print ("-"*70)
