def H (n, ç1, ç2, ç3): # Hanoi kulesi...
    global sayaç
    print ("H:", n, ç1, ç2, ç3, sep="")
    if n > 0:
        H (n - 1, ç1, ç3, ç2)
        if ç1[0]:
            disk = ç1[0].pop()
            print ("==>taþý disk:" + str(disk) + "'i " + ç1[1] + "'den " + ç3[1] + "'e.")
            ç3[0].append (disk)
        H (n - 1, ç2, ç1, ç3)
        sayaç +=1

sayaç = 0
ç1 = ([3,2,1], "ç1")
ç3 = ([], "ç3")
ç2 = ([], "ç2")
print ("Ýlk:", ç1, ç2, ç3, "\n")
H (len (ç1[0]), ç1, ç2, ç3)
print ("\nSon: ", ç1, " ", ç2, " ", ç3, " Hamle:2^", len(ç3), "-1=", sayaç, sep="")



