import numpy as np

X = np.array ([
    [[3, 1, 2], [4, 2, 2], [2, 4, 1]],
    [[3, 2, 2], [4, 4, 3], [4, 1, 1]],
    [[2, 2, 1], [3, 1, 3], [3, 2, 3]] ]) # X(3,3,3) matrisi...

Y = np.array ([
    [[2, 3, 1], [2, 2, 4], [3, 4, 4]],
    [[1, 4, 1], [4, 1, 2], [4, 1, 2]],
    [[1, 2, 3], [4, 1, 1], [3, 1, 4]] ]) # Y(3,3,3) matrisi...

Z = np.dot (X, Y)
print ("Þekilleri: X", X.shape, " * Y(", Y.shape, " = Z", Z.shape, sep="")
print("\nX, Y ve sonuç çarpým matrisleri:")
print (X, "\n---\n", Y, "\n---\n", Z, sep="")



