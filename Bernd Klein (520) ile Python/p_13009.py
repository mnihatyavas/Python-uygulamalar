import re

with open ("p_13009x1.txt") as almanPK:
    þehirKodlarý = {}
    for satýr in almanPK:
        sonuç = re.search (r"[\d ]+([^\d]+[a-z])\s(\d+)", satýr)
        if sonuç:
            þehir, postaKodu = sonuç.groups()
