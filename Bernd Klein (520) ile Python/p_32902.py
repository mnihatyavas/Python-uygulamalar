def faktörKümesi():
    küme = ( (i, j, k, l)
        for i in [-1, 0, 1]
        for j in [-1, 0, 1]
        for k in [-1, 0, 1]
        for l in [-1, 0, 1])
    for faktör in küme: yield faktör

def belle (f):
    sonuçlar = {}
    def yardýmcý (n):
        if n not in sonuçlar: sonuçlar [n] = f (n)
        return sonuçlar [n]
    return yardýmcý

@belle
def doðrusalBirleþtirme (n):
    # n = i*1 + j*3 + k*9 + l*27 sonucunu döndürecek tüple (i,j,k,l) üretir...
    aðýrlýklar = (1, 3, 9, 27)
    for elemanlar in faktörKümesi():
       toplam = 0
       uz = len (elemanlar)
       for i in range (uz): toplam += elemanlar [i] * aðýrlýklar [i]
       if toplam == n: return elemanlar

