liste = [ 'abcd', 786 , 2.23, 'Nihat', 23.5E-7J, 70.2 ]
küçükListe = [123, 'Nihat']

print (liste) # Tüm listeyi yazar...
print (liste[0]) # Listenin ilk elemanýný yazar...
print (liste[1:3]) # Listenin 2 ve 3. elemanlarýný yazar...
print (liste[2:]) # Listenin 2.den sona kadarý yazar...
print (küçükListe * 3) # Listeyi 3 kez ardýþýk yazar...
print (liste + küçükListe, "\n") # Küçük listeyi listeye ekleyerek yazar...

